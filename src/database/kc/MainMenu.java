        int percent = ((current*100)/1000);
        jProgressBar2.setStringPainted(true);
        jProgressBar2.setString(percent+"%");
        jProgressBar2.setValue(0);
        jProgressBar2.setOpaque(true);
        try {
            int i = 0;
            while (i<=percent){
                if(percent<25){
                    jProgressBar2.setBackground(Color.red);
                    jProgressBar2.setForeground(Color.red);
                }else if(percent >=25 && percent<75){
                    jProgressBar2.setBackground(Color.yellow);
                    jProgressBar2.setForeground(Color.yellow);
                }else{
                    jProgressBar2.setBackground(Color.green);
                    jProgressBar2.setForeground(Color.green);
                }
                jProgressBar2.setValue(i+10);
                Thread.sleep(1);
                i+=20;
            }
        } catch (Exception e) {
            System.out.println("SomethingWrong"+e);
        }

